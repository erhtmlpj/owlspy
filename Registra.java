        Usuario usuario = new Usuario();
        Conta conta = new Conta();
        DAOUsuario daousuario = new DAOUsuario();
        DAOConta daoconta = new DAOConta();

        if (request.getParameter("mail") != null) {
            usuario.setEmail(request.getParameter("mail"));
            conta.setEmail(usuario.getEmail());
        }

        if ((request.getParameter("senha1") != null) && (request.getParameter("senha2") != null)) {
            if (request.getParameter("senha1").equals(request.getParameter("senha2"))) {
                usuario.setSenha(request.getParameter("senha1"));
            }
        }

        if (request.getParameter("login") != null) {
            conta.setLogin(request.getParameter("login"));
        }

        daousuario.insere(usuario);
        daoconta.insere(conta);
        response.sendRedirect("login.html");
        
